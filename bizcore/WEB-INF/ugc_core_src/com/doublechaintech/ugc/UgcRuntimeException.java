
package com.doublechaintech.ugc;


public class UgcRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public UgcRuntimeException() {
        super();
    }


    public UgcRuntimeException(String message) {
        super(message);
    }


    public UgcRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public UgcRuntimeException(Throwable cause) {
        super(cause);
    }


   
}












